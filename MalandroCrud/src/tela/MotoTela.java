    private void btnLisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLisActionPerformed
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        model.setRowCount(0);

        for (Moto mo : daoi.listar()) {
            model.addRow(new Object[]{
                mo.getVin(),
                mo.getCor(),
                mo.getMarca(),
                mo.getPeso()
            });
        }
    }//GEN-LAST:event_btnLisActionPerformed

    private void btnExcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcActionPerformed
        int row = tabela.getSelectedRow(); // Verificar se alguma linha foi selecionada

        if (row == -1) { // Nenhuma linha selecionada
            JOptionPane.showMessageDialog(this, "Selecione um registro para excluir!");
            return;
        }

        int vin = Integer.parseInt(tabela.getValueAt(row, 0).toString()); 

        int confirmacao = JOptionPane.showConfirmDialog(this,
            "Tem certeza que deseja excluir a Moto com Vin: " + vin + " ?",
            "Confirmação", JOptionPane.YES_NO_OPTION);

        if (confirmacao == JOptionPane.YES_OPTION) {
            daoi.deletar(vin);  // chama o DAO para excluir
            btnLisActionPerformed(null); // atualiza a tabela
            JOptionPane.showMessageDialog(this, "Moto excluida com sucesso!");
        }
    }//GEN-LAST:event_btnExcActionPerformed

