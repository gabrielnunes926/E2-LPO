    private void btnRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegActionPerformed

        Moto mo = new Moto();
        mo.setVin(Integer.parseInt(txtVin.getText()));
        mo.setCor(txtCor.getText());
        mo.setMarca(txtMar.getText());
        mo.setPeso(Integer.parseInt(txtPes.getText()));

        try {
            daoi.inserir(mo);
        } catch (SQLException ex) {
            System.getLogger(MalandroTela.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
        daoi.listar(); // Atualiza a tabela

    }//GEN-LAST:event_btnRegActionPerformed

